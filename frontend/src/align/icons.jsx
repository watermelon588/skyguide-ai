/**
 * Inline Feather icons for the companion (MIT — feathericons.com).
 *
 * Deliberately NOT react-icons: importing it here fuses the whole icon pack
 * into the vendor chunk both entries share, and the phone ends up
 * downloading every icon the desktop app uses (~90KB gz, measured). Eleven
 * hand-inlined paths keep the companion bundle honest. Same 24×24 stroked
 * geometry, so they read identically to the app's react-icons/fi set.
 */

